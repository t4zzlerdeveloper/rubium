function EmojiSelector(props) {

    const [search,setSearch] = useState("");

    const user = useUser();
    const lang = new LangTranslator("EmojiSelector",user);
    
    return(<div className="emjsel">
        <input value={search} onChange={(e)=>{setSearch(e.target.value)}} placeholder={lang.tr('Search Emojis...')}/>
        <div>
            {emojis.filter((e)=>{
                if(e.toLowerCase().includes(search.toLowerCase())){
                    return e;
                }
            }).map((e)=>{
                return <Emoji size={"20px"} name={e} onClick={()=>{props.onSelect(e)}}/>
            })}
        </div>
    </div>)

}

export default EmojiSelector